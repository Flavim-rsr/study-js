// Aula 01 - Console.log();
// Console.log é um metodo do Js utilizado para debug
// ; é opcional porem é recomendado utilizar para praticar
// Dentro do console.log podemos colocar qualquer tipo de dado, como string, number, boolean, array, object, etc.
// Quando for utilizar um valor literal, como string, number, boolean, etc., devemos colocar entre aspas simples ou duplas.
// Js é uma linguagem de tipagem dinamica

console.log("Hello, 'World!'");      // Caso eu queira exibir aspas dentro do meu texto preciso colocar a aspa ao contrario do que esta por fora
console.log('Hello, "World!"');  
console.log(`Hello, World!`);       // Template strings posso utilizar aspas simples e duplas sem quebrar o codigo

// Tambem é possivel exibir numeros no console.log();
console.log(123);
console.log(15.85);

// Posso exibir mais de uma coisa ao mesmo tempo no mesmo console.log sem quebrar a linha
console.log(35, 15.85, 'Flavio Rodrigo De Souza');

